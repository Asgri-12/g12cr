st.subheader("Leukemia")
st.write("""Cancers that begin in the blood-forming tissue of the bone marrow are called leukemias. These cancers do not form solid tumors. Instead, large numbers of abnormal white blood cells (leukemia cells and leukemic blast cells) build up in the blood and bone marrow, crowding out normal blood cells. The low level of normal blood cells can make it harder for the body to get oxygen to its tissues, control bleeding, or fight infections.  

There are four common types of leukemia, which are grouped based on how quickly the disease gets worse (acute or chronic) and on the type of blood cell the cancer starts in (lymphoblastic or myeloid). Acute forms of leukemia grow quickly and chronic forms grow more slowly.""")
st.subheader("Colorectal")
st.write("""Colorectal cancer is a disease in which cells in the colon or rectum grow out of control. Sometimes it is called colon cancer, for short. The colon is the large intestine or large bowel. The rectum is the passageway that connects the colon to the anus.

Sometimes abnormal growths, called polyps, form in the colon or rectum. Over time, some polyps may turn into cancer. Screening tests can find polyps so they can be removed before turning into cancer. Screening also helps find colorectal cancer at an early stage, when treatment works best.""")
st.subheader("Prostate")
st.write("""Prostate cancer is cancer that occurs in the prostate. The prostate is a small walnut-shaped gland in males that produces the seminal fluid that nourishes and transports sperm.

Prostate cancer is one of the most common types of cancer. Many prostate cancers grow slowly and are confined to the prostate gland, where they may not cause serious harm. However, while some types of prostate cancer grow slowly and may need minimal or even no treatment, other types are aggressive and can spread quickly.

Prostate cancer that's detected early — when it's still confined to the prostate gland — has the best chance for successful treatment.""")
st.subheader("Non-Hodgkins")
st.write("""Non-Hodgkin's lymphoma is a type of cancer that begins in your lymphatic system, which is part of the body's germ-fighting immune system. In non-Hodgkin's lymphoma, white blood cells called lymphocytes grow abnormally and can form growths (tumors) throughout the body.

Non-Hodgkin's lymphoma is a general category of lymphoma. There are many subtypes that fall in this category. Diffuse large B-cell lymphoma and follicular lymphoma are among the most common subtypes. The other general category of lymphoma is Hodgkin's lymphoma.

Advances in diagnosis and treatment of non-Hodgkin's lymphoma have helped improve the prognosis for people with this disease.""")
st.subheader("Brain cancer")
st.write("""Primary brain cancer, also known simply as brain cancer, is an overgrowth of cells in your brain that forms masses called brain tumors. This is different than cancer which starts in another part of your body and spreads to your brain. When that happens, it’s called secondary or metastasized brain cancer.

Some types of cancerous brain tumors can grow very quickly. These malignant tumors can disrupt the way your body works. Brain tumors can be life threatening and need to be treated as soon as they’re detected.

Brain cancer is quite uncommon. According to estimates from the American Cancer Society, people have less than a 1 percentTrusted Source chance of developing a malignant brain tumor in their lifetime.""")
st.subheader("Breast")
st.write("""Breast cancer is cancer that forms in the cells of the breasts.

After skin cancer, breast cancer is the most common cancer diagnosed in women in the United States. Breast cancer can occur in both men and women, but it's far more common in women.

Substantial support for breast cancer awareness and research funding has helped create advances in the diagnosis and treatment of breast cancer. Breast cancer survival rates have increased, and the number of deaths associated with this disease is steadily declining, largely due to factors such as earlier detection, a new personalized approach to treatment and a better understanding of the disease.""")
st.subheader("Thyroid")
st.write("""Thyroid cancer is a growth of cells that starts in the thyroid. The thyroid is a butterfly-shaped gland located at the base of the neck, just below the Adam's apple. The thyroid produces hormones that regulate heart rate, blood pressure, body temperature and weight.

Thyroid cancer might not cause any symptoms at first. But as it grows, it can cause signs and symptoms, such as swelling in your neck, voice changes and difficulty swallowing.

Several types of thyroid cancer exist. Most types grow slowly, though some types can be very aggressive. Most thyroid cancers can be cured with treatment.""")



